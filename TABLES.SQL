CREATE TABLE student (
    AUTOID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(50) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL UNIQUE,
    COURSE_NAME VARCHAR(8) NOT NULL,
	SEMESTER VARCHAR(5) NOT NULL,
	PASSWORD VARCHAR(1000)
);


